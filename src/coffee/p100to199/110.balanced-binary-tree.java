class Solution110 {
    public boolean isBalanced(TreeNode root) {
        return balance(root) != -1;
    }

    private int balance(TreeNode root) {
        if (root == null) {
            return 0;
        }
        int left = balance(root.left);
        int right = balance(root.right);
        if (left == -1 || right == -1) {
            return -1;
        }
        if (Math.abs(left - right) > 1) {
            return -1;
        }
        return 1 + Math.max(left, right);
    }
}
