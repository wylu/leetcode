class Solution662 {
    private int res = 0;
    private HashMap<Integer, Integer> left = new HashMap<>();

    /**
     * 1.以深度优先顺序遍历每个节点，并跟踪该节点的位置。
     * 2.对于每个深度，到达该深度的第一个节点的位置将保持在left[depth]中。
     * 3.然后，对于每个节点，候选宽度为pos-left[depth]+1。我们采用候选答案的最大值。
     *
     * @param root 树的根结点
     * @return 最大宽度
     */
    public int widthOfBinaryTree(TreeNode root) {
        dfs(root, 0, 0);
        return res;
    }

    public void dfs(TreeNode root, int depth, int pos) {
        if (root == null) return;
        left.putIfAbsent(depth, pos);
        res = Math.max(res, pos - left.get(depth) + 1);
        dfs(root.left, depth + 1, 2 * pos);
        dfs(root.right, depth + 1, 2 * pos + 1);
    }
}
